sub old_transpose {
    my ( $c, $xpose ) = @_;
    return $c unless $xpose;
    assert_tuning();
    return $c unless $c =~ m/
				^ (
				    [CF](?:is|\#)? |
				    [DG](?:is|\#|es|b)? |
				    A(?:is|\#|s(?!us)|b)? |
				    E(?:s(?!us)|b)? |
				    B(?:es|b)? |
				    H
				  )
				  (.*)
			    /x;
    my ( $r, $rest ) = ( $1, $2 );
    my $mod = 0;
    $mod-- if $r =~ s/(e?s|b)$//;
    $mod++ if $r =~ s/(is|\#)$//;
    warn("WRONG NOTE: '$c' '$r' '$rest'") unless defined $notes2canon{$r};
    $r = ($notes2canon{$r} + $mod + $xpose) % 12;

    if ( $rest =~ m;^(.*)/(.*); ) {
	$rest = $1 . "/" . transpose( $2, $xpose );
    }

    return ( $xpose > 0 ? \@notes_sharp : \@notes_flat )->[$r] . $rest;
}


